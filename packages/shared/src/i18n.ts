// ── Types & resolver ────────────────────────────────────────────────────────

type DeepStringRecord = {
  readonly [key: string]: string | readonly string[] | DeepStringRecord;
};
export type Translations = DeepStringRecord;

const dictionaries: Record<Locale, Translations> = { ru, en };

/**
 * Resolve a translation key like 'dashboard.s2sTitle' for the given locale,
 * substituting {var} placeholders from `vars`. Returns the path itself if missing.
 */
export function t(locale: Locale, path: string, vars?: Record<string, string | number>): string {
  const keys = path.split('.');
  let value: unknown = dictionaries[locale];
  for (const key of keys) {
    if (value && typeof value === 'object' && key in (value as object)) {
      value = (value as Record<string, unknown>)[key];
    } else {
      return path;
    }
  }
  if (typeof value !== 'string') return path;
  if (!vars) return value;
  return value.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}

/** Get an array translation (used for month name arrays). */
export function tArray(locale: Locale, path: string): readonly string[] {
  const keys = path.split('.');
  let value: unknown = dictionaries[locale];
  for (const key of keys) {
    if (value && typeof value === 'object' && key in (value as object)) {
      value = (value as Record<string, unknown>)[key];
    } else {
      return [];
    }
  }
  return Array.isArray(value) ? value : [];
}

