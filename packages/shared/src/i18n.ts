/** Format an amount in minor units (kopecks/cents) to a display string. */
export function formatMoney(amountMinor: number, currency: 'RUB' | 'USD' | string, locale: Locale = 'en'): string {
  const major = amountMinor / 100;
  const intlLocale = locale === 'ru' ? 'ru-RU' : 'en-US';
  if (currency === 'USD') {
    return `$${major.toLocaleString(intlLocale, { minimumFractionDigits: 0, maximumFractionDigits: 0 })}`;
  }
  // Default to RUB-style: number then symbol
  const sym = currency === 'RUB' ? '₽' : currency;
  return `${major.toLocaleString(intlLocale, { minimumFractionDigits: 0, maximumFractionDigits: 0 })} ${sym}`;
}

/** Format an integer count using locale conventions (thousand separators). */
export function formatNumber(n: number, locale: Locale): string {
  return n.toLocaleString(locale === 'ru' ? 'ru-RU' : 'en-US', { maximumFractionDigits: 0 });
}

/** Capitalized short month name (Янв / Jan). */
export function monthShortCap(monthIdx: number, locale: Locale): string {
  const arr = tArray(locale, 'monthsShortCap');
  return arr[monthIdx] ?? '';
}

/** Genitive short month name (lowercase: янв, used in "5 янв" date format). */
export function monthShortGen(monthIdx: number, locale: Locale): string {
  const arr = tArray(locale, 'monthsShortGen');
  return arr[monthIdx] ?? '';
}

/** "{day} {month}" — e.g. "5 янв" / "Jan 5". */
export function formatDayMonth(date: Date, locale: Locale): string {
  const day = date.getDate();
  const month = monthShortGen(date.getMonth(), locale);
  return locale === 'en' ? `${month} ${day}` : `${day} ${month}`;
}

/** "Сегодня" / "Today" / "Вчера" / "Yesterday" / "{day} {month}". */
export function formatDayLabel(d: Date, locale: Locale): string {
  const today = new Date();
  if (d.toDateString() === today.toDateString()) return t(locale, 'common.today');
  const yest = new Date(today);
  yest.setDate(yest.getDate() - 1);
  if (d.toDateString() === yest.toDateString()) return t(locale, 'common.yesterday');
  return formatDayMonth(d, locale);
}

/** "Янв 5 → Фев 12" / "Jan 5 → Feb 12" — short period range with capitalized months. */
export function formatPeriodLabel(start: Date | string, end: Date | string, locale: Locale): string {
  const s = typeof start === 'string' ? new Date(start) : start;
  const e = typeof end === 'string' ? new Date(end) : end;
  return `${monthShortCap(s.getMonth(), locale)} ${s.getDate()} → ${monthShortCap(e.getMonth(), locale)} ${e.getDate()}`;
}

